# Escreva um programa que leia três números e mostre qual é o maior e qual o menor.
numero1 = int(input("Digite o número A: "))
numero2 = int(input("Digite o número B: "))
numero3 = int(input("Digite o número C: "))

print("O menor número é: ", min(numero1, numero2, numero3))
print("O maior número é: ", max(numero1, numero2, numero3))
